    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        btnSua.setEnabled(false);
        btnXoa.setEnabled(false);
        int selectedIndex = tblPhong.getSelectedRow();
        if (selectedIndex >= 0) {
            phong std = phongList.get(selectedIndex);
            String maPhong = std.getMaPhong();
            int option = JOptionPane.showConfirmDialog(this, "Bạn chắc chắn muốn xóa dữ liệu này?");
            System.out.println("option : " + option);
            if (option == 0) {
                tableModel.removeRow(selectedIndex);
                conn = con.getConnection();
                try {
                    String sql = "DELETE FROM dbo.tbl_trangthaiphong WHERE maPhong = ?";
                    PreparedStatement statement = conn.prepareCall(sql);
                    System.out.println(maPhong);
                    statement.setString(1, maPhong);
                    statement.execute();

                    sql = "DELETE FROM dbo.tbl_phong WHERE maPhong = ?";
                    statement = conn.prepareCall(sql);
                    statement.setString(1, maPhong);
                    statement.execute();

                } catch (Exception e) {
                } finally {
                    if (conn != null) {
                        try {
                            conn.close();
                        } catch (SQLException ex) {
                            Logger.getLogger(LoginForm.class.getName()).log(Level.SEVERE, null, ex);
                        }
                    }
                }
            }
            show_phong();
            autoNullColumn();
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        // TODO add your handling code here:
        if (selectedIndex >= 0) {
            if (txtTenPhong.getText().equals("")) {
                System.out.println("Tên phòng không được trống");
                JOptionPane.showMessageDialog(this, "Tên phòng không được trống");
                txtTenPhong.requestFocus(true);
                txtTenPhong.selectAll();
            } else if (txtMoTa.getText().equals("")) {
                System.out.println("Phần mô tả không được trống");
                JOptionPane.showMessageDialog(this, "Phần mô tả không được trống");
                txtMoTa.requestFocus(true);
                txtMoTa.selectAll();
            } else if (txtLoaiPhong.getText().equals("")) {
                System.out.println("Loại phòng không được trống");
                JOptionPane.showMessageDialog(this, "Loại phòng không được trống");
                txtLoaiPhong.requestFocus(true);
                txtLoaiPhong.selectAll();
            } else if (!txtMax.getText().trim().matches("^[1-9][0-9]*$")) {
                System.out.println("Số người tối ta phải là số nguyên dương");
                JOptionPane.showMessageDialog(this, "Số người tối ta phải là số nguyên dương");
                txtMax.requestFocus(true);
                txtMax.selectAll();
            } else {
                int option = JOptionPane.showConfirmDialog(this, "Bạn chắc chắn muốn sửa dữ liệu này?");
                System.out.println("option : " + option);
                if (option == 0) {
                    tableModel.removeRow(selectedIndex);
                    conn = con.getConnection();
                    try {
                        String sql = "UPDATE dbo.tbl_phong SET tenPhong=?,maLoaiPhong=?,mota=?,songuoitoida=? WHERE maPhong=?";
                        PreparedStatement statement = conn.prepareCall(sql);
                        System.out.println(txtMaPhong.getText());

                        statement.setString(1, txtTenPhong.getText());
                        statement.setString(2, txtLoaiPhong.getText());
                        statement.setString(3, txtMoTa.getText());
                        statement.setInt(4, Integer.valueOf(txtMax.getText()));
                        statement.setString(5, txtMaPhong.getText().trim());

                        statement.execute();
                        JOptionPane.showMessageDialog(this, "Sửa dữ liệu thành công");

                        autoNullColumn();
                        btnXoa.setEnabled(false);
                        btnSua.setEnabled(false);
                    } catch (Exception e) {
                    } finally {
                        if (conn != null) {
                            try {
                                conn.close();
                            } catch (SQLException ex) {
                                Logger.getLogger(main_phong.class.getName()).log(Level.SEVERE, null, ex);
                            }
                        }
                    }
                }
            }
            show_phong();
        }
    }//GEN-LAST:event_btnSuaActionPerformed

