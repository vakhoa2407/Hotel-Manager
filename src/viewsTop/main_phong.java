    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
        if (txtMaPhong.getText().trim().equals("")) {
            System.out.println("Mã phòng không được trống");
            JOptionPane.showMessageDialog(this, "Mã phòng không được trống");
            txtMaPhong.requestFocus(true);
            txtMaPhong.selectAll();
        } else if (txtTenPhong.getText().trim().equals("")) {
            System.out.println("Tên phòng không được trống");
            JOptionPane.showMessageDialog(this, "Tên phòng không được trống");
            txtTenPhong.requestFocus(true);
            txtTenPhong.selectAll();
        } else if (txtLoaiPhong.getText().trim().equals("")) {
            System.out.println("Loại phòng không được trống");
            JOptionPane.showMessageDialog(this, "Loại phòng không được trống");
            txtLoaiPhong.requestFocus(true);
            txtLoaiPhong.selectAll();
        } else if (txtMoTa.getText().trim().equals("")) {
            System.out.println("Phần mô tả không được trống");
            JOptionPane.showMessageDialog(this, "Phần mô tả không được trống");
            txtMoTa.requestFocus(true);
            txtMoTa.selectAll();
        } else if (txtMax.getText().trim().equals("")) {
            System.out.println("Số người tối đa không được trống");
            JOptionPane.showMessageDialog(this, "Số người tối đa của phòng không được trống");
            txtMax.requestFocus(true);
            txtMax.selectAll();
        } else if (!txtMax.getText().trim().matches("^[1-9][0-9]*$")) {
            System.out.println("Số người tối đa phải là số nguyên dương");
            JOptionPane.showMessageDialog(this, "Số người tối ta phải là số nguyên dương");
            txtMax.requestFocus(true);
            txtMax.selectAll();
        } else {
            conn = con.getConnection();
            try {
                String sql = "INSERT INTO dbo.tbl_phong(maPhong,tenPhong,maLoaiPhong,mota,songuoitoida)VALUES(?, ?, ?, ?, ?)";
                PreparedStatement statement = conn.prepareCall(sql);
                //
                //PreparedStatement pst = conn.prepareStatement(sql_login);
                statement.setString(1, txtMaPhong.getText());
                statement.setString(2, txtTenPhong.getText());
                statement.setString(3, txtLoaiPhong.getText());
                statement.setString(4, txtMoTa.getText());
                statement.setInt(5, Integer.valueOf(txtMax.getText()));

                ResultSet rs = statement.executeQuery();
                while (rs.next()) {
                    if (rs.getInt("ketqua") == -2) {
                        System.out.println("Loại phòng không tồn tại");
                        JOptionPane.showMessageDialog(this, "Loại phòng không tồn tại");
                        txtLoaiPhong.requestFocus(true);
                        txtLoaiPhong.selectAll();
                    } else if (rs.getInt("ketqua") == -1) {
                        System.out.println("Số người tối đa không được âm");
                        JOptionPane.showMessageDialog(this, "Số người tối đa không được âm");
                        txtMax.requestFocus(true);
                        txtMax.selectAll();
                    } else if (rs.getInt("ketqua") == 0) {
                        System.out.println("Thêm phòng thất bại");
                        JOptionPane.showMessageDialog(this, "Phòng đã được thêm trước đó\nThêm phòng thất bại");
                    } else if (rs.getInt("ketqua") == 1) {
                        System.out.println("Thêm phòng thành công");
                        JOptionPane.showMessageDialog(this, "Thêm phòng thành công");
                        autoNullColumn();
                        btnLuu.setEnabled(false);
                    }
                }
            } catch (Exception e) {
            } finally {
                if (conn != null) {
                    try {
                        conn.close();
                    } catch (SQLException ex) {
                        Logger.getLogger(main_phong.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }
        }
        show_phong();
    }//GEN-LAST:event_btnLuuActionPerformed

